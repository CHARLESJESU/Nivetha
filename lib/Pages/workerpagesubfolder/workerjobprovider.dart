

class JobProvider {
  final String name;
  final String gender;
  final String dob;
  final String email;
  final String phone;
  final String address;
  final String area;
  final String city;
  final String district;
  final String state;
  final String country;
  final String experience;
  final String role;
  final String profileImageBase64;

  JobProvider({
    required this.name,
    required this.gender,
    required this.dob,
    required this.email,
    required this.phone,
    required this.address,
    required this.area,
    required this.city,
    required this.district,
    required this.state,
    required this.country,
    required this.experience,
    required this.role,
    required this.profileImageBase64,
  });
}
